package com.spring.board.vo;

public class DateHeader {
	String rStart;
	String rEnd;
	String cStart;
	String cEnd;
	String dRow;
	String dCol;
	
	public String getrStart() {
		return rStart;
	}
	public void setrStart(String rStart) {
		this.rStart = rStart;
	}
	public String getrEnd() {
		return rEnd;
	}
	public void setrEnd(String rEnd) {
		this.rEnd = rEnd;
	}
	public String getcStart() {
		return cStart;
	}
	public void setcStart(String cStart) {
		this.cStart = cStart;
	}
	public String getcEnd() {
		return cEnd;
	}
	public void setcEnd(String cEnd) {
		this.cEnd = cEnd;
	}
	public String getdRow() {
		return dRow;
	}
	public void setdRow(String dRow) {
		this.dRow = dRow;
	}
	public String getdCol() {
		return dCol;
	}
	public void setdCol(String dCol) {
		this.dCol = dCol;
	}
	
	
}
